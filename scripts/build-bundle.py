#!/usr/bin/env python3
"""
Build script for amplifier-webruntime.js

This script:
1. Takes wheel files and amplifier_webruntime.py
2. Encodes them as base64
3. Generates amplifier-webruntime.js with embedded assets

Usage:
    python scripts/build-bundle.py \
        --core-wheel /path/to/amplifier_core-X.X.X-py3-none-any.whl \
        --foundation-wheel /path/to/amplifier_foundation-X.X.X-py3-none-any.whl \
        --output dist/amplifier-webruntime.js

If wheel paths are not provided, the script will attempt to:
1. Build them from local repos (if available)
2. Download them from GitHub releases
"""

import argparse
import base64
import hashlib
import subprocess
import sys
import tempfile
from pathlib import Path

BUNDLE_ROOT = Path(__file__).parent.parent
SRC_DIR = BUNDLE_ROOT / "src"
JS_SRC = SRC_DIR / "js" / "amplifier-webruntime.src.js"
DIST_DIR = BUNDLE_ROOT / "dist"

VERSION = "0.1.0"


def compute_hash(content: bytes) -> str:
    """Compute short hash for verification."""
    return hashlib.sha256(content).hexdigest()[:8]


def encode_file(path: Path) -> tuple[str, str]:
    """Encode file to base64 and return (encoded, hash)."""
    content = path.read_bytes()
    encoded = base64.b64encode(content).decode("ascii")
    return encoded, compute_hash(content)


def find_or_build_wheel(
    package_name: str, local_repo: Path | None = None
) -> Path | None:
    """Find existing wheel or build from local repo."""

    # Check if we have a local repo
    if local_repo and local_repo.exists():
        print(f"Building {package_name} from {local_repo}...")

        # Build wheel using uv
        with tempfile.TemporaryDirectory() as tmpdir:
            result = subprocess.run(
                ["uv", "build", "--wheel", "--out-dir", tmpdir],
                cwd=local_repo,
                capture_output=True,
                text=True,
            )

            if result.returncode == 0:
                # Find the built wheel
                wheels = list(Path(tmpdir).glob("*.whl"))
                if wheels:
                    # Copy to dist
                    dest = DIST_DIR / wheels[0].name
                    dest.write_bytes(wheels[0].read_bytes())
                    print(f"  Built: {dest.name}")
                    return dest
            else:
                print(f"  Build failed: {result.stderr}")

    return None


